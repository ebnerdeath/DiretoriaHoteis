    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        int linha_selecionada = tblClientes.getSelectedRow();
        int coluna = 0;
        if(linha_selecionada>-1){
           int id = (int) tblClientes.getValueAt(linha_selecionada,coluna);
           nova_reserva.retornaCliente(id);
           this.dispose();
        }else{
           JOptionPane.showMessageDialog(null,"É Necessário escolher um cliente para continuar!","Atenção",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

