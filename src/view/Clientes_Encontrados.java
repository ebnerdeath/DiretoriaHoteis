    //MÉTODO QUE POPULA A TABELA DE CLIENTES
    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
        modelo.setNumRows(0);
        ClienteDAO clientedao = new ClienteDAO();        
        //FOR DE PESQUISA NO BANCO CONTENDO NOME DO CLIENTE
        for(Cliente cliente: clientedao.read()){
                modelo.addRow(new Object[]{
                cliente.getId(),
                cliente.getNome(),
                cliente.getCpf(),
                cliente.getTel_fixo(),
                cliente.getTel_cel()
                });   
            SetaLargura_Jtable();
            Centraliza_Dados();
        }
    }    
        
