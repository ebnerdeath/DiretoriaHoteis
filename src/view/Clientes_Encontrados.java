    public void Centraliza_Dados(){
        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        
        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        
        tblClientes.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        tblClientes.getColumnModel().getColumn(1).setCellRenderer(esquerda);
        tblClientes.getColumnModel().getColumn(2).setCellRenderer(centralizado);
        tblClientes.getColumnModel().getColumn(3).setCellRenderer(centralizado);  
    }
    
    public void SetaLargura_Jtable(){
        tblClientes.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tblClientes.getColumnModel().getColumn(0).setPreferredWidth(55);
        tblClientes.getColumnModel().getColumn(1).setPreferredWidth(383);
        tblClientes.getColumnModel().getColumn(2).setPreferredWidth(120);
        tblClientes.getColumnModel().getColumn(3).setPreferredWidth(120);
    }
    
