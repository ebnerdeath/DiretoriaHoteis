    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        if(tblClientes.getSelectedRow() != -1){
            Limpa_Campos();
            ClienteDAO clientedao = new ClienteDAO();
            flag = 2;

            ativa_Campos();

            int id = (int) tblClientes.getValueAt(tblClientes.getSelectedRow(),0);

            for(Cliente cliente: clientedao.read_Method(id)){

                txtNome.setText(cliente.getNome());
                comboPessoa.setSelectedItem(String.valueOf(cliente.getTipoPessoa()));
                jftxtData_Nasc.setText(cliente.getData_nasc());
                jftxtRg.setText(cliente.getRg());
                jftxtCpf.setText(cliente.getCpf());
                jftxtCnpj.setText(cliente.getCnpj());
                ComboSexo.setSelectedItem(String.valueOf(cliente.getSexo()));
                jftxtTelFixo.setText(cliente.getTel_fixo());
                jftxtTelCel.setText(cliente.getTel_cel());
                txtEmail.setText(cliente.getEmail());
                jftxtCep.setText(cliente.getCep());
                txtEndereco.setText(cliente.getEndereco());
                txtNumero.setText(String.valueOf(cliente.getNumero()));
                ComboUF.setSelectedItem(String.valueOf(cliente.getUf()));
                txtCidade.setText(cliente.getCidade());
                txtComplemento.setText(cliente.getComplemento());

            }
            btnNovo.setEnabled(false);
            btnSalvar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnCancelar.setEnabled(true);

        }

    }//GEN-LAST:event_tblClientesMouseClicked

