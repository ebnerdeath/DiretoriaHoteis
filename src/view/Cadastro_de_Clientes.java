    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if((tblClientes.getSelectedRow() != -1)){
            Cliente cliente = new Cliente();
            ClienteDAO clientedao = new ClienteDAO();

            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?",getTitle(),+ JOptionPane.YES_NO_OPTION);

            if(resposta==JOptionPane.YES_OPTION){
                cliente.setId((int)tblClientes.getValueAt(tblClientes.getSelectedRow(),0));

                clientedao.delete(cliente);
                readJTable();
                Limpa_Campos();
                tblClientes.clearSelection();
                btnExcluir.setEnabled(false);
                btnSalvar.setEnabled(false);
                btnCancelar.setEnabled(false);
            }else{
                tblClientes.clearSelection();
                btnExcluir.setEnabled(false);
                btnSalvar.setEnabled(false);
                btnCancelar.setEnabled(false);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Selecione um cliente!");
        }
        btnNovo.setEnabled(true);
        comboPessoa.setSelectedIndex(0);
        desativa_Campos();
    }//GEN-LAST:event_btnExcluirActionPerformed

